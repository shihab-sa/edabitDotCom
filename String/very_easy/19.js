/*

isNaN() মানে নট এ নাম্বার, নাম্বার বাদে string সত্য(  single string )

*/

// প্রথমে নাম্বার দিয়ে চেক করলাম

let number = 23
console.log(isNaN(number));



//পরে string দিয়ে চেক করলাম

let singleString = 'e'
console.log(isNaN(singleString));


let singleString2 = '20'
console.log(isNaN(singleString2));