// Convert Age to Days
// Create a function that takes the age in years and returns the age in days.

// Examples
// calcAge(65) ➞ 23725

// calcAge(0) ➞ 0

// calcAge(20) ➞ 7300


function calcAge(age){
    return age*365

}

console.log(calcAge(65));// ➞ 23725
console.log(calcAge(0)); //➞ 0
console.log(calcAge(20));// ➞ 7300
